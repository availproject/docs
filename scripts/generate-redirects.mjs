import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const OLD_URLS_PATH = fileURLToPath(
  new URL("../data/old-docs-urls.json", import.meta.url),
);
const NEW_URLS_PATH = fileURLToPath(
  new URL("../data/new-docs-urls.json", import.meta.url),
);
const OVERRIDES_PATH = fileURLToPath(
  new URL("../data/redirects.override.json", import.meta.url),
);

const GENERATED_JSON_PATH = fileURLToPath(
  new URL("../data/redirects.generated.json", import.meta.url),
);
const GENERATED_TS_PATH = fileURLToPath(
  new URL("../src/lib/redirects/generated.ts", import.meta.url),
);
const RUNTIME_RULES_PATH = fileURLToPath(
  new URL("../data/redirects.runtime.json", import.meta.url),
);

function normalizePath(p) {
  if (!p) return "/";
  const stripped = p.replace(/\/+$/, "");
  return stripped === "" ? "/" : stripped;
}

function tokenize(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9/_-]+/g, " ")
    .split(/[\s/_-]+/g)
    .map((t) => t.trim())
    .filter(Boolean)
    .filter(
      (t) =>
        ![
          "to",
          "and",
          "the",
          "of",
          "on",
          "a",
          "an",
          "with",
          "for",
          "in",
          "overview",
        ].includes(t),
    );
}

function jaccard(aTokens, bTokens) {
  const a = new Set(aTokens);
  const b = new Set(bTokens);
  let intersection = 0;
  for (const t of a) if (b.has(t)) intersection += 1;
  const union = a.size + b.size - intersection;
  return union === 0 ? 0 : intersection / union;
}

function bestPrefixRuleMatch(path, prefixRules) {
  const normalized = normalizePath(path);
  const candidates = prefixRules
    .map((r) => ({ ...r, source: normalizePath(r.source) }))
    .sort((a, b) => b.source.length - a.source.length);

  const best = candidates.find(
    (r) => normalized === r.source || normalized.startsWith(`${r.source}/`),
  );
  if (!best) return null;

  const suffix = normalized.slice(best.source.length);
  return {
    source: best.source,
    destination: normalizePath(best.destination) + suffix,
  };
}

function inferSection(path) {
  if (path === "/") return "root";
  const seg = path.split("/").filter(Boolean)[0];
  return seg ?? "other";
}

async function readJson(path) {
  return JSON.parse(await readFile(path, "utf8"));
}

function renderGeneratedTs(rules) {
  const lines = [];
  lines.push('import type { RedirectRule } from "./types";');
  lines.push("");
  lines.push("/**");
  lines.push(" * Generated by scripts/generate-redirects.mjs");
  lines.push(" * Do not edit by hand.");
  lines.push(" */");
  lines.push("export const generatedRedirectRules: RedirectRule[] = [");
  for (const r of rules) {
    lines.push(
      `  { source: ${JSON.stringify(r.source)}, destination: ${JSON.stringify(r.destination)} },`,
    );
  }
  lines.push("];");
  lines.push("");
  return lines.join("\n");
}

async function main() {
  const oldPaths = (await readJson(OLD_URLS_PATH)).map(normalizePath);
  const newItems = await readJson(NEW_URLS_PATH);
  const overrides = await readJson(OVERRIDES_PATH);

  const newPaths = new Set(newItems.map((i) => normalizePath(i.path)));
  const newByPath = new Map(newItems.map((i) => [normalizePath(i.path), i]));

  const staticPrefixRules = [
    { source: "/da", destination: "/docs/da" },
    { source: "/nexus", destination: "/docs/nexus" },
    { source: "/user-guides", destination: "/docs/da/user-guides" },
  ];
  const staticExactRules = [
    // Legacy safety net (not in current sitemap, but historically linked).
    { source: "/api-reference", destination: "/docs/da/api-reference" },
  ];

  const sectionFallbackDestination = {
    da: "/docs/da/build",
    nexus: "/docs/nexus/get-started",
    "user-guides": "/docs/da/user-guides",
    root: "/docs",
    other: "/docs",
  };

  const overrideSources = new Set(
    overrides.map((r) => normalizePath(r.source)),
  );

  const newIndex = Array.from(newPaths).map((path) => {
    const meta = newByPath.get(path);
    const pathTokens = tokenize(path);
    const titleTokens = tokenize(meta?.title ?? "");
    return { path, pathTokens, titleTokens };
  });

  const generatedMappings = [];

  for (const oldPath of oldPaths) {
    if (overrideSources.has(oldPath)) continue;

    const section = inferSection(oldPath);

    const staticApplied = bestPrefixRuleMatch(oldPath, staticPrefixRules);
    if (
      staticApplied &&
      newPaths.has(normalizePath(staticApplied.destination))
    ) {
      // Static prefix redirect will land on a valid new page.
      continue;
    }

    // Specialized transforms (before fuzzy).
    const directTransforms = [];

    // Nexus SDK moved from /nexus/avail-nexus-sdk/* to /docs/nexus/sdk/* (with some reference paths under /reference).
    if (
      oldPath === "/nexus/avail-nexus-sdk" ||
      oldPath.startsWith("/nexus/avail-nexus-sdk/")
    ) {
      const rest = oldPath.slice("/nexus/avail-nexus-sdk".length);
      const isReferenceSection =
        /^\/(bridge-methods|swap-methods|hooks-and-errors|api-reference|utility-reference)\b/i.test(
          rest,
        );
      const dstBase = isReferenceSection
        ? "/docs/nexus/sdk/reference"
        : "/docs/nexus/sdk";
      directTransforms.push(dstBase + rest);
    }

    if (section === "da" || section === "nexus") {
      directTransforms.push(`/docs${oldPath}`);
      directTransforms.push(`/docs${oldPath.toLowerCase()}`);
    } else if (section === "user-guides") {
      directTransforms.push(`/docs/da${oldPath}`);
      directTransforms.push(`/docs/da${oldPath.toLowerCase()}`);
    }

    const directDestination = directTransforms.find((p) =>
      newPaths.has(normalizePath(p)),
    );
    if (directDestination) {
      generatedMappings.push({
        source: oldPath,
        destination: normalizePath(directDestination),
        confidence: 1,
        reason: "transform:direct",
      });
      continue;
    }

    // Fuzzy match fallback
    const oldTokens = tokenize(oldPath);
    const oldLast = oldPath.split("/").filter(Boolean).slice(-1)[0] ?? "";
    const preferPrefix =
      section === "da"
        ? "/docs/da/"
        : section === "nexus"
          ? "/docs/nexus/"
          : section === "user-guides"
            ? "/docs/da/user-guides/"
            : "/docs/";

    let best = { path: "/docs", score: 0, reason: "fallback:/docs" };

    for (const candidate of newIndex) {
      let score = 0;
      score += 0.7 * jaccard(oldTokens, candidate.pathTokens);
      score += 0.3 * jaccard(oldTokens, candidate.titleTokens);

      const newLast =
        candidate.path.split("/").filter(Boolean).slice(-1)[0] ?? "";
      if (
        oldLast &&
        newLast &&
        oldLast.toLowerCase() === newLast.toLowerCase()
      ) {
        score += 0.25;
      }

      if (candidate.path.startsWith(preferPrefix)) score += 0.1;
      if (candidate.path.includes("/_archive/")) score -= 0.05;

      if (score > best.score) {
        best = {
          path: candidate.path,
          score,
          reason: "fuzzy:path+title",
        };
      }
    }

    const fallback =
      sectionFallbackDestination[section] ?? sectionFallbackDestination.other;
    const destination = best.score >= 0.3 ? best.path : fallback;
    const confidence = Math.max(0, Math.min(1, best.score));

    generatedMappings.push({
      source: oldPath,
      destination,
      confidence,
      reason: best.reason,
    });
  }

  // Sort for deterministic output (low confidence first is better for review).
  generatedMappings.sort(
    (a, b) => a.confidence - b.confidence || a.source.localeCompare(b.source),
  );

  await mkdir(dirname(GENERATED_JSON_PATH), { recursive: true });
  await writeFile(
    GENERATED_JSON_PATH,
    `${JSON.stringify(generatedMappings, null, 2)}\n`,
    "utf8",
  );

  const generatedRules = generatedMappings.map(({ source, destination }) => ({
    source,
    destination,
  }));

  await writeFile(GENERATED_TS_PATH, renderGeneratedTs(generatedRules), "utf8");

  const runtimeRules = [
    ...overrides.map((r) => ({
      source: normalizePath(r.source),
      destination: normalizePath(r.destination),
    })),
    ...generatedRules.map((r) => ({
      source: normalizePath(r.source),
      destination: normalizePath(r.destination),
    })),
    ...staticExactRules.map((r) => ({
      source: normalizePath(r.source),
      destination: normalizePath(r.destination),
    })),
    ...staticPrefixRules.map((r) => ({
      source: normalizePath(r.source),
      destination: normalizePath(r.destination),
    })),
  ];

  await writeFile(
    RUNTIME_RULES_PATH,
    `${JSON.stringify(runtimeRules, null, 2)}\n`,
    "utf8",
  );

  process.stdout.write(
    `Generated ${generatedMappings.length} explicit redirects -> ${GENERATED_JSON_PATH}\n`,
  );
}

await main();
